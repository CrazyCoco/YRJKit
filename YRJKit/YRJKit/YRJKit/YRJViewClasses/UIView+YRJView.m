+ (instancetype)yrj_loadViewFromNib
{
    return [self yrj_loadViewFromNibWithName:NSStringFromClass([self class])];
}

+ (instancetype)yrj_loadViewFromNibWithName:(NSString *)nibName
{
    return [self yrj_loadViewFromNibWithName:nibName owner:nil];
}

+ (instancetype)yrj_loadViewFromNibWithName:(NSString *)nibName owner:(id)owner
{
    return [self yrj_loadViewFromNibWithName:nibName owner:owner bundle:[NSBundle mainBundle]];
}

+ (instancetype)yrj_loadViewFromNibWithName:(NSString *)nibName owner:(id)owner bundle:(NSBundle *)bundle
{
    UIView *result = nil;
    NSArray* elements = [bundle loadNibNamed:nibName owner:owner options:nil];
    for (id object in elements)
    {
        if ([object isKindOfClass:[self class]])
        {
            result = object;
            break;
        }
    }
    return result;
}


